# Comece com uma lista e use um FOR LOOP para percorrer a lista e imprima cada váriavel precedida pelo texto "I like"


coin = ['EUR', 'USD', 'BRL'] # Definindo váriaveis.

for coin in coin:                   # Para cada moeda que ele passar dentro da lista de moedas, até chegar ao final da lista.
    print('I Like ' + coin)         # Exibe  I like + todas váriaveis.